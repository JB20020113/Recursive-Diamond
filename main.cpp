#include <iostream>
#include <vector>
using namespace std;

// Recursive function to print the lines
void printRecursive(const vector<string>& lines, int index) {
    if (index == lines.size())
        return; // base case

    cout << lines[index] << endl;

    printRecursive(lines, index + 1); // recursive step
}

int main() {
    vector<string> lines = {
"*************************",
"***********   ***********",
"**********  *  **********",
"*********  ***  *********",
"********  *****  ********",
"*******  *******  *******",
"******  *********  ******",
"*****  ***********  *****",
"****  *************  ****",
"***  ***************  ***",
"**  *****************  **",
"*  *******************  *",
"**  *****************  **",
"***  ***************  ***",
"****  *************  ****",
"*****  ***********  *****",
"******  *********  ******",
"*******  *******  *******",
"********  *****  ********",
"*********  ***  *********",
"**********  *  **********",
"***********   ***********",
"*************************"
    };

    printRecursive(lines, 0);

    return 0;
}
